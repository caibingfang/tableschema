package com.tool.common;

import java.util.List;

public class DataBaseElement {

	private String ip;
	
	private String port;
	
	private String db;
	
	private String username;
	
	private String password;
	

	public String getIp() {
		return ip;
	}

	public DataBaseElement setIp(String ip) {
		this.ip = ip;
		return this;
	}

	public String getPort() {
		return port;
	}

	public DataBaseElement setPort(String port) {
		this.port = port;
		return this;
	}

	public String getDb() {
		return db;
	}

	public DataBaseElement setDb(String db) {
		this.db = db;
		return this;
	}

	public String getUsername() {
		return username;
	}

	public DataBaseElement setUsername(String username) {
		this.username = username;
		return this;
	}

	public String getPassword() {
		return password;
	}

	public DataBaseElement setPassword(String password) {
		this.password = password;
		return this;
	}

	
	
}
